			if (APIParams.HAS_GPRS != null) {
				URI.append("&has_gprs=");
				URI.append(APIParams.HAS_GPRS);

			}
			if (APIParams.HAS_GPS != null) {
				URI.append("&has_gps=");
				URI.append(APIParams.HAS_GPS);

			}
			if (APIParams.HAS_GPU != null) {
				URI.append("&has_gpu=");
				URI.append(APIParams.HAS_GPU);

			}
			if (APIParams.HAS_RADIO != null) {
				URI.append("&has_radio=");
				URI.append(APIParams.HAS_RADIO);

			}
